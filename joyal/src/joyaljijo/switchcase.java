package joyaljijo;

public class switchcase {

	public static void main(String[] args) {
		int a=10;
		switch(a) {
		case 10:System.out.println("hello");
		break;
		case 20:System.out.println("hi");
		break;
		case 30:System.out.println("good morning");
		break;
		default:System.out.println("invalid");
		}

	}

}
